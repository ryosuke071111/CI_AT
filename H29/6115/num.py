def recognizeNumHash(m):
    hashNum = 0
    counter = 0
    for line in m:
        for c in line:
            if c == "*" : tmp = 1
            if c == " " : tmp = 0
            if c == "|" : tmp = 3
            counter += 1
            hashNum += (counter * ((513) ** tmp)) % 4755137
    return hashNum

